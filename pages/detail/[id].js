const API_KEY = process.env.API_KEY;

export async function getServerSideProps(context) {
    const {id} = context.query;
    
    const detail = await fetch(    
        `https://api.themoviedb.org/3/movie/${id}?api_key=${API_KEY}`,
        {
            where: {
                    id: id
                }})
            .then((res) => res.json());
            // console.log(detail);
        
        return {
            props: {
                detail
            },
        }
    }
        